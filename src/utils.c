#include "fdf.h"

void	print_map(t_fdf *map)
{
	int i = 0;
	int j = 0;
	
	while (i < map->rows)
	{
		j = 0;
		while (j < map->cols)	
		{
			printf("%d  ", map->maps[i][j]);
			j++;
		}
		printf("\n");
		i++;
	}
}
